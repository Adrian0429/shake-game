function ModalForm({ onSubmit, isOpen, close }: ModalAllowProps) {
  const {
    register,
    handleSubmit,
    watch,
    formState: { errors },
    setValue,
  } = useForm<FormData>({
    defaultValues: {
      email: "",
      region: "ID", // Set the default region to Indonesia (using the code)
    },
  });


  const [regions, setRegions] = useState(allRegions);

  const updateKYC = async (data: FormData) => {
    try {
      const response = await fetch("/api/submit", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
        },
        body: JSON.stringify(data),
      });

      const result = await response.json();
      console.log("Form Submitted Successfully:", result);
    } catch (error) {
      console.error("Error submitting form:", error);
    }
  };

  const regionInput = watch("region");
  const filteredRegions = regions.filter(
    (region) =>
      region.name.toLowerCase().includes(regionInput?.toLowerCase()) ||
      region.code.toLowerCase().includes(regionInput?.toLowerCase())
  );

  if (!isOpen) return null;

  return (
    <dialog className="fixed left-0 top-0 w-full h-full bg-black bg-opacity-50 z-50 overflow-auto flex justify-center items-center">
      <div className="relative flex flex-col items-center mx-auto py-4 px-6 bg-[#232328] min-h-[300px] w-[350px] rounded-lg z-100">
        <div className="flex w-full flex-row justify-between">
          <p className="text-[#232328]">x</p>
          <h1 className="text-2xl font-bold text-center text-white">
            Fill in your Data Here!
          </h1>
          <p onClick={close} className="text-2xl font-bold text-white">x</p>
        </div>

        <form
          onSubmit={handleSubmit((data) => {
            updateKYC(data);
            onSubmit();
          })}
          className="flex flex-col w-full items-center mt-4 space-y-4"
        >
          {/* Email input */}
          <div className="w-[80%]">
            <input
              type="email"
              placeholder="Email"
              className="w-full h-12 px-3 rounded-lg text-black"
              {...register("email", { required: "Email is required" })}
            />
            {errors.email && (
              <p className="text-red-500">{errors.email.message}</p>
            )}
          </div>

          <div className="form-group w-[80%] mt-3">
            <select
              id="region"
              className="w-full h-12 px-3 rounded-lg text-black"
              {...register("region", { required: "Region is required" })}
            >
              {regions.map((region) => (
                <option key={region.code} value={region.code}>
                  {region.name}
                </option>
              ))}
            </select>
            {errors.region && <p className="error">{errors.region.message}</p>}
          </div>

          {/* Submit button */}
          <button
            type="submit"
            className="w-[80%] mt-4 h-12 bg-[#D5FF18] cursor-pointer select-none active:translate-y-2
              active:[box-shadow:0_0px_0_0_#ABC340,0_0px_0_0_#ffffff]
              active:border-b-[0px] transition-all duration-150 
              [box-shadow:0_2px_0_0_#ABC340,0_4px_0_0_#ffffff]
              rounded-full border-[1px] border-[#D5FF18] mb-3"
          >
            <span className="flex justify-center items-center h-full text-black font-bold text-2xl">
              submit
            </span>
          </button>
        </form>
      </div>
    </dialog>
  );
}

